const paymentStatus: Record<string, string> = {
  unpaid: "Non payé",
  partial: "Partiel",
  paid_stripe: "Payé (Stripe)",
  paid_cash: "Payé (espèces)",
  paid_transfer: "Payé (virement)",
  paid_mb: "Payé (Multibanco)",
  paid_card: "Payé (carte)",
  paid_other: "Payé (autre)",
};

export function formatAdminReservationStatus(status: string): string {
  return reservationStatus[status] ?? status;
}

export function formatAdminPaymentStatus(status: string): string {
  return paymentStatus[status] ?? status;
}

/** Affichage 24h (HH:mm). */
export function formatTime24h(time: string): string {
  return time;
}

export function formatReceptionHours24h(open: string, close: string): string {
  return `${formatTime24h(open)} – ${formatTime24h(close)}`;
}

